"""
Before and after examples, generated from the committed run files, never
written by hand. For each example the script prints the question, the full
retrieved list, the full answer and the automatic scores under both
configurations, so the README can show complete evidence instead of a
summary of it. Writes outputs/before_after.md and prints the same markdown
to stdout for pasting into the README.

The pairs are chosen where exactly one controlled change flips the result:

  q05  bm25 -> hybrid       retrieval representation (the vocabulary gap)
  q14  bm25 -> hybrid       retrieval, both sides of a comparison
  q04  bm25 -> dense        retrieval, morphology (and hybrid loses this
                            one again, stated in the note)
  q17  grounded -> permissive   answer policy, retrieval identical
  q08  extractive -> generative answer layer, retrieval identical

    python3 -m eval.build_before_after
"""
import json, os
from eval.score_answers import score_run

HERE = os.path.dirname(os.path.abspath(__file__))
OUT = os.path.join(HERE, "..", "outputs")

EXAMPLES = [
    ("q05", "bm25_baseline", "hybrid_baseline",
     "One change: the retriever, BM25 to hybrid (BM25 + MiniLM dense under "
     "reciprocal rank fusion). Same chunks, same k, same grounded answer "
     "layer. The refusal flips to a correct definitional answer because "
     "dense retrieval surfaces Art. 1, whose definition the sparse "
     "retrievers never find behind the ubiquitous usage of 'commanding "
     "officer'. The related q07 is the counterexample where retrieval "
     "recovery alone does not flip the answer; see the failure analysis."),
    ("q14", "bm25_baseline", "hybrid_baseline",
     "One change: the retriever, BM25 to hybrid. A comparative question "
     "needs both gold articles in the top k."),
    ("q04", "bm25_baseline", "dense_baseline",
     "One change: the retriever, BM25 to dense (MiniLM). Note for balance: "
     "the hybrid fusion loses this question again, because chunks present "
     "in both top 50 lists outvote dense's single confident ranking; that "
     "trade is discussed in the README ablations."),
    ("q08", "hybrid_baseline", "hybrid_generative",
     "One change: the answer layer, grounded extractive to generative "
     "(flan-t5-small), retrieval identical (hybrid). Scores come from the "
     "committed score files for each run."),
]

POLICY_EXAMPLE = ("q17", "grounded", "permissive",
                  "One change: the answer policy. Retrieval is identical in "
                  "both arms (BM25 baseline); the permissive arm skips the "
                  "support test, the punitive article guard and the corpus "
                  "boundary checks.")


def _load(tag):
    with open(os.path.join(OUT, f"run_{tag}.json")) as f:
        run = json.load(f)
    scores = {s["id"]: s for s in
              score_run(os.path.join(OUT, f"run_{tag}.json"))["per_question"]}
    return {r["id"]: r for r in run["records"]}, scores


def _fmt_retrieved(retrieved):
    lines = []
    for i, r in enumerate(retrieved):
        lines.append("%d. Art. %s, %s (score %.4f)"
                     % (i + 1, r["article"], r["title"], r["score"]))
    return "\n".join(lines)


def _fmt_scores(rec, s):
    retr = rec["retrieval"]
    bits = []
    if retr["scored"]:
        bits.append("first gold rank %s" % (retr["first_gold_rank"] or "MISS"))
        bits.append("all gold retrieved %s" % retr["all_gold_retrieved"])
    bits.append("refused %s" % s["refused"])
    if s["citation_hit"] is not None:
        bits.append("citation hit %s" % s["citation_hit"])
        bits.append("citation precision %s" % s["citation_precision"])
        bits.append("content coverage %s" % s["content_coverage"])
    bits.append("support ok %s" % s["support_ok"])
    return ", ".join(bits)


def _quote(text):
    return "\n".join("> " + line for line in text.split("\n"))


def _arm(label, config_desc, rec, s):
    return [
        "**%s** (%s)\n" % (label, config_desc),
        "Retrieved:\n\n%s\n" % _fmt_retrieved(rec["retrieved"]),
        "Answer:\n\n%s\n" % _quote(rec["answer"]["text"]),
        "Scores: %s\n" % _fmt_scores(rec, s),
    ]


def build(out_dir=OUT):
    md = ["# Before and after examples\n",
          "Generated by `python3 -m eval.build_before_after` from the "
          "committed run files; every retrieved list, answer and score below "
          "is read from `outputs/`, not written by hand.\n"]
    with open(os.path.join(HERE, "questions.json")) as f:
        questions = {q["id"]: q for q in json.load(f)}

    n = 0
    for qid, before_tag, after_tag, note in EXAMPLES:
        b_path = os.path.join(OUT, f"run_{before_tag}.json")
        a_path = os.path.join(OUT, f"run_{after_tag}.json")
        if not (os.path.exists(b_path) and os.path.exists(a_path)):
            print("skipping %s (%s or %s missing)" % (qid, before_tag, after_tag))
            continue
        b_recs, b_scores = _load(before_tag)
        a_recs, a_scores = _load(after_tag)
        q = questions[qid]
        n += 1
        md.append("## Example %d: %s, %s to %s\n" % (n, qid, before_tag, after_tag))
        md.append("**Question.** %s\n" % q["question"])
        arts = q["gold_articles"]
        md.append("**Gold.** %s\n" % (
            ("Article%s %s" % ("s" if len(arts) > 1 else "", ", ".join(arts)))
            if arts else "control, correct behavior is a refusal"))
        md.append("%s\n" % note)
        md += _arm("Before", before_tag, b_recs[qid], b_scores[qid])
        md += _arm("After", after_tag, a_recs[qid], a_scores[qid])

    # the policy example lives in the ablation file, both arms in one record
    abl_path = os.path.join(OUT, "answer_policy_ablation.json")
    if os.path.exists(abl_path):
        with open(abl_path) as f:
            abl = json.load(f)
        qid, b_arm, a_arm, note = POLICY_EXAMPLE
        rec = next(r for r in abl["records"] if r["id"] == qid)
        b_run, b_scores = _load("bm25_baseline")  # identical retrieval
        pq = {p: {s["id"]: s for s in abl["per_question_scores"][p]}
              for p in ("grounded", "permissive")}
        n += 1
        q = questions[qid]
        md.append("## Example %d: %s, %s to %s policy\n" % (n, qid, b_arm, a_arm))
        md.append("**Question.** %s\n" % q["question"])
        md.append("**Gold.** control, correct behavior is a boundary refusal "
                  "(the true figure lives in the Manual for Courts-Martial, "
                  "not the statute)\n")
        md.append("%s\n" % note)
        md.append("Retrieved (identical in both arms):\n\n%s\n"
                  % _fmt_retrieved(b_run[qid]["retrieved"]))
        for label, arm in (("Before", b_arm), ("After", a_arm)):
            s = pq[arm][qid]
            md.append("**%s** (%s policy)\n" % (label, arm))
            md.append("Answer:\n\n%s\n" % _quote(rec[arm]["text"]))
            md.append("Scores: refused %s, refusal correct %s\n"
                      % (s["refused"], s["refusal_correct"]))

    text = "\n".join(md)
    path = os.path.join(out_dir, "before_after.md")
    with open(path, "w") as f:
        f.write(text)
    print(text)
    print("\nwrote %s (%d examples)" % (path, n))
    return n


if __name__ == "__main__":
    build()
