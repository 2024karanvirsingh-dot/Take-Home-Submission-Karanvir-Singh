"""
Human readable per question report for a run, generated from the structured
run and score files (never hand maintained). Writes:

  outputs/evaluation_report.md    one section per question with the full
                                  answer, retrieved article sequence, all
                                  automatic metrics and a rule derived
                                  failure category
  outputs/question_results.csv    the same, one row per question

Default run is the final selected system (hybrid retrieval + grounded
extractive answers). The failure category is assigned by fixed rules over
the automatic metrics, so it is reproducible; the qualitative reading of
each category stays in the README.

    python3 -m eval.build_report                      # hybrid_baseline
    python3 -m eval.build_report bm25_baseline
"""
import csv, json, os, sys
from eval.score_answers import score_run

HERE = os.path.dirname(os.path.abspath(__file__))
OUT = os.path.join(HERE, "..", "outputs")

DEFAULT_TAG = "hybrid_baseline"


def failure_category(rec, score):
    """Fixed rules over the automatic metrics. Returned as
    (category, one line interpretation)."""
    retr = rec["retrieval"]
    gold = rec["gold_articles"]
    retrieved_arts = [r["article"] for r in rec["retrieved"]]
    if not rec["answerable_from_corpus"]:
        if score["refusal_correct"]:
            return ("control_refused_correctly",
                    "control question, correctly refused at the corpus boundary")
        return ("control_answered",
                "control question answered instead of refused, the worst outcome")
    miss = retr["first_gold_rank"] == 0
    if score["refused"]:
        if miss:
            return ("false_refusal_after_retrieval_miss",
                    "gold article %s never retrieved; the grounded policy "
                    "converted the miss into a refusal instead of a wrong "
                    "answer" % "/".join(gold))
        return ("false_refusal_after_retrieval_hit",
                "gold article retrieved but no sentence cleared the support "
                "test")
    parts = []
    if not score["citation_hit"]:
        cat = ("misattribution_after_retrieval_miss" if miss
               else "misattribution")
        return (cat,
                "answered from %s but gold is %s%s"
                % ("/".join(rec["answer"]["cited_articles"]) or
                   retrieved_arts[0],
                   "/".join(gold),
                   "; gold was never retrieved" if miss else ""))
    if not score["support_ok"]:
        parts.append("support check failed")
    if not retr["all_gold_retrieved"]:
        parts.append("only one side of a multi article question retrieved")
    cov = score["content_coverage"]
    if cov is not None and cov < 1.0:
        parts.append("content coverage %.2f, part of the required wording "
                     "missing" % cov)
    if parts:
        return ("partial", "; ".join(parts))
    return ("success", "correct citation, full required content, support "
                       "check passed")


def build(tag, out_dir=OUT):
    run_path = os.path.join(OUT, f"run_{tag}.json")
    with open(run_path) as f:
        run = json.load(f)
    scored = score_run(run_path)
    scores = {s["id"]: s for s in scored["per_question"]}
    with open(os.path.join(HERE, "questions.json")) as f:
        questions = {q["id"]: q for q in json.load(f)}

    rows, md = [], []
    cfg = run["config"]
    md.append("# Per question evaluation report: %s\n" % tag)
    md.append("Generated by `python3 -m eval.build_report %s` from "
              "`outputs/run_%s.json`; do not edit by hand. Configuration: "
              "retriever %s, answerer %s, policy %s, chunk_words %s, k %s.\n"
              % (tag, tag, cfg["retriever"], cfg.get("answerer", "extractive"),
                 cfg.get("policy", "grounded"), cfg["chunk_words"], cfg["k"]))
    for rec in run["records"]:
        q = questions[rec["id"]]
        s = scores[rec["id"]]
        cat, why = failure_category(rec, s)
        retrieved_seq = " > ".join(r["article"] for r in rec["retrieved"])
        gold = "/".join(rec["gold_articles"]) or "(control)"
        rank = (rec["retrieval"].get("first_gold_rank", "")
                if rec["retrieval"]["scored"] else "")
        row = {
            "id": rec["id"], "type": rec["type"],
            "question": rec["question"],
            "answerable_from_corpus": rec["answerable_from_corpus"],
            "gold_articles": gold,
            "retrieved_articles": retrieved_seq,
            "first_gold_rank": rank,
            "all_gold_retrieved": (rec["retrieval"].get("all_gold_retrieved", "")
                                   if rec["retrieval"]["scored"] else ""),
            "refused": s["refused"],
            "cited_articles": "/".join(rec["answer"]["cited_articles"]),
            "citation_hit": s["citation_hit"],
            "citation_precision": s["citation_precision"],
            "content_coverage": s["content_coverage"],
            "support_ok": s["support_ok"],
            "numbers_supported": s.get("numbers_supported"),
            "category": cat,
            "interpretation": why,
            "answer": rec["answer"]["text"],
        }
        rows.append(row)
        md.append("## %s (%s)\n" % (rec["id"], rec["type"]))
        md.append("**Question.** %s\n" % rec["question"])
        md.append("**Gold.** %s. **Retrieved (in rank order).** %s. "
                  "**First gold rank.** %s\n"
                  % (gold, retrieved_seq, rank if rank != "" else "n/a"))
        md.append("**Answer%s.**\n\n%s\n"
                  % (" (refused)" if s["refused"] else
                     ", citing " + (row["cited_articles"] or "nothing"),
                     _quote(rec["answer"]["text"])))
        md.append("**Scores.** citation_hit %s, citation_precision %s, "
                  "content_coverage %s, support_ok %s, numbers_supported %s\n"
                  % (s["citation_hit"], s["citation_precision"],
                     s["content_coverage"], s["support_ok"],
                     s.get("numbers_supported")))
        md.append("**Category.** `%s`: %s\n" % (cat, why))

    md_path = os.path.join(out_dir, "evaluation_report.md")
    with open(md_path, "w") as f:
        f.write("\n".join(md))
    csv_path = os.path.join(out_dir, "question_results.csv")
    with open(csv_path, "w", newline="") as f:
        w = csv.DictWriter(f, fieldnames=list(rows[0].keys()))
        w.writeheader()
        w.writerows(rows)
    print("wrote %s and %s (%d questions)" % (md_path, csv_path, len(rows)))
    return rows


def _quote(text):
    return "\n".join("> " + line for line in text.split("\n"))


if __name__ == "__main__":
    build(sys.argv[1] if len(sys.argv) > 1 else DEFAULT_TAG)
